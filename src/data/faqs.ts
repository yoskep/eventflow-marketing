/**
 * Returns service-specific FAQs merged with shared FAQs.
 * Service-specific questions come first, followed by shared ones.
 */
export function getFaqsForService(serviceSlug: string): FAQItem[] {
  const specific = serviceFaqs[serviceSlug] || [];
  return [...specific, ...sharedFaqs];
}

// Keep backward compatibility for imports
export const genericFaqs: FAQItem[] = sharedFaqs;

export function interpolateFaqs(
  faqs: FAQItem[],
  serviceNameHe: string,
  cityNameHe: string,
): FAQItem[] {
  return faqs.map((faq) => ({
    question: faq.question.replace(/\{service\}/g, serviceNameHe).replace(/\{city\}/g, cityNameHe),
    answer: faq.answer.replace(/\{service\}/g, serviceNameHe).replace(/\{city\}/g, cityNameHe),
  }));
}
